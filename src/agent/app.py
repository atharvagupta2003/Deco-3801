from flask import Flask, request, jsonify
import os
import logging
from flask_cors import CORS
from graph import setup_workflow
from graph import workflow, graph

app = Flask(__name__)
CORS(app)

# Logging configuration for better debugging
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')

UPLOAD_FOLDER = 'uploads'
ALLOWED_EXTENSIONS = {'txt', 'csv', 'pdf'}
app.config['UPLOAD_FOLDER'] = UPLOAD_FOLDER
app.config['MAX_CONTENT_LENGTH'] = 16 * 1024 * 1024  # 16 MB file limit

@app.route('/health', methods=['GET'])
def health_check():
    return jsonify({'status': 'healthy'}), 200

def run_graph_workflow(question: str):
    """
    Runs the AI graph workflow.
    Args:
        question (str): User query.

    Returns:
        str: Generated answer.
    """
    if graph is None:
        logging.error("Graph is not initialized.")
        return "Error: Graph not initialized."

    inputs = {"question": question}
    config = {
        "configurable": {
            "thread_id": "1",
            "checkpoint_ns": "default_ns",
            "checkpoint_id": "checkpoint_1"
        }
    }
    output = None
    try:
        for event in graph.stream(inputs, stream_mode="values", config=config):
            output = event
            if 'generation' in output:
                generation = output['generation']
                return generation.content
            else:
                logging.warning(f"Unexpected event structure: {output}")
        if not output:
            logging.error("No output received from the graph.")
            return "Error: No output received."
    except Exception as e:
        logging.error(f"Error in graph processing: {str(e)}")
        return f"Error during processing: {str(e)}"

    logging.error("No 'generation' found in output.")
    return "Error: No generation found."

@app.route('/ask', methods=['POST'])
def ask():
    try:
        data = request.json
        if 'question' not in data:
            return jsonify({'error': 'No question provided'}), 400

        question = data['question']
        logging.info(f"Received question: {question}")
        answer = run_graph_workflow(question)

        if answer:
            logging.info(f"Answer generated for question: {question}")
            return jsonify({'answer': answer}), 200
        else:
            return jsonify({'error': 'No answer generated by the AI.'}), 500
    except Exception as e:
        logging.error(f"Error in ask: {str(e)}")
        return jsonify({'error': f'Internal server error: {str(e)}'}), 500

if __name__ == '__main__':
    os.makedirs(UPLOAD_FOLDER, exist_ok=True)
    app.run(debug=True, host='0.0.0.0', port=5050)